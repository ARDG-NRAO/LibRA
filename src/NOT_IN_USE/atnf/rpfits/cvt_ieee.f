      subroutine VAXR4 (rv, rl)
*-----------------------------------------------------------------------
*   VAXR4 converts from VAX REAL to little-endian IEEE REAL.
*
*   $Id: cvt_ieee.f,v 1.4 2001/02/08 05:09:08 mcalabre Exp $
*-----------------------------------------------------------------------
      real      rl, rv
*-----------------------------------------------------------------------
      call RV2L (rv, rl)
      return
      end



      subroutine VAXI4 (iv, il)
*-----------------------------------------------------------------------
*   VAXI4 converts (copies) VAX INTEGER to little-endian INTEGER.
*-----------------------------------------------------------------------
      integer   il, iv
*-----------------------------------------------------------------------
      il = iv
      return
      end



      subroutine R4VAX (rl, rv)
*-----------------------------------------------------------------------
*   R4VAX converts from little-endian IEEE REAL to VAX REAL.
*-----------------------------------------------------------------------
      real      rl, rv
*-----------------------------------------------------------------------
      call RL2V (rl, rv)
      return
      end



      subroutine I4VAX (il, iv)
*-----------------------------------------------------------------------
*   I4VAX converts (copies) little-endian INTEGER to VAX INTEGER.
*-----------------------------------------------------------------------
      integer   il, iv
*-----------------------------------------------------------------------
      iv = il
      return
      end
